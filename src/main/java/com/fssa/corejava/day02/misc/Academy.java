package com.fssa.corejava.day02.misc;

public class Academy {
	String name;
	int id;
	String code;
	
	
	public String getName() {
		return name;
	}
	public void setName(String newname) {
		name = newname;
	}
	public int getId() {
		return id;
	}
	public void setId(int newid) {
		id = newid;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String newcode) {
		code = newcode;
	}
	
	
}
